      totalEarned: state.totalEarned,
      totalSeconds: state.totalSeconds,
      uniqueGames: uniquePlayed(state),
      purchases: Object.assign({}, state.purchases),
      equipped: Object.assign({}, state.equipped),
      awards: state.awards.slice(),
